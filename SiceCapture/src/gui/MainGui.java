    private void createExpedientButtonActionPerformed(java.awt.event.ActionEvent evt) {
        boolean validProcess = true;
        if ("".equals(nameExpedientTxt.getText().trim())) {
            validProcess = false;
        } else if ("".equals(descriptionExpedientTxt.getText().trim())) {
            validProcess = false;
        }
        if (validProcess) {
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            Expedient expedient = new Expedient();
            expedient.setName(nameExpedientTxt.getText().trim());
            expedient.setDescription(descriptionExpedientTxt.getText().trim());
            List<Document> selectedDocuments = new ArrayList<>();
            List selectedNameDocumentsList = ExpedientDocumentList.getSelectedValuesList();
            if (selectedNameDocumentsList != null && !selectedNameDocumentsList.isEmpty()) {
                for (Object selectedDocumentName : selectedNameDocumentsList) {
                    Document selectedDocument = documentController.findByName(selectedDocumentName.toString());
                    if (selectedDocument != null) {
                        selectedDocuments.add(selectedDocument);
                    }
                }
            }
            expedient.setDocumentCollection(selectedDocuments);
            expedientController.create(expedient);
            em.getTransaction().commit();
            initExpedientTree();
        }
    }

